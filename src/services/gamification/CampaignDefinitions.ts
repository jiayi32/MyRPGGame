/** Lookup a primary class by ID. */
export function getPrimaryClass(id: string): PrimaryClassDefinition | undefined {
  return PRIMARY_CLASS_DEFINITIONS.find(c => c.id === id);
}

/** Lookup a secondary class by ID. */
export function getSecondaryClass(id: string): SecondaryClassDefinition | undefined {
  return SECONDARY_CLASS_DEFINITIONS.find(c => c.id === id);
}

/** Get all skills (active + passive) for a primary class up to a given rank. */
export function getUnlockedSkills(classId: string, rank: number): (SkillDefinition | PassiveDefinition)[] {
  const cls = getPrimaryClass(classId);
  if (!cls) return [];
  return cls.skills.filter(s => s.unlockRank <= rank);
}

