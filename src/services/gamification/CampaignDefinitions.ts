function validateTickConfigCoverage(): void {
  const allSkills: SkillDefinition[] = [
    ...PRIMARY_CLASS_DEFINITIONS.flatMap(cls =>
      cls.skills.filter((s): s is SkillDefinition => s.skillType !== 'passive'),
    ),
    ...SECONDARY_CLASS_DEFINITIONS.flatMap(cls =>
      cls.skills.filter((s): s is SkillDefinition => s.skillType !== 'passive'),
    ),
    ...ENEMY_SKILLS,
  ];

  const DOT_SUBTYPES = new Set(['poison', 'burn', 'bleed']);
  const HOT_SUBTYPES = new Set(['regen']);
  const VALID_POLICIES = new Set(['refresh', 'stack', 'unique']);

  const missing: string[] = [];
  const invalid: string[] = [];
  const isValidTick = (value: number, durationTurns: number) =>
    Number.isFinite(value)
    && Number.isFinite(durationTurns)
    && value >= 0
    && value <= 1
    && durationTurns >= 1;

  for (const skill of allSkills) {
    if (skill.effectTags.includes('hot') && !skill.tickConfig?.hot) {
      missing.push(`${skill.id} (missing tickConfig.hot)`);
    } else if (skill.tickConfig?.hot) {
      const hot = skill.tickConfig.hot;
      if (!isValidTick(hot.value, hot.durationTurns)) {
        invalid.push(`${skill.id} (invalid tickConfig.hot)`);
      }
      if (!HOT_SUBTYPES.has(hot.subtype)) {
        invalid.push(`${skill.id} (tickConfig.hot.subtype must be one of ${[...HOT_SUBTYPES].join('|')}, got '${hot.subtype}')`);
      }
      if (hot.stackPolicy && !VALID_POLICIES.has(hot.stackPolicy)) {
        invalid.push(`${skill.id} (tickConfig.hot.stackPolicy invalid: '${hot.stackPolicy}')`);
      }
    }
    if (skill.effectTags.includes('dot') && !skill.tickConfig?.dot) {
      missing.push(`${skill.id} (missing tickConfig.dot)`);
    } else if (skill.tickConfig?.dot) {
      const dot = skill.tickConfig.dot;
      if (!isValidTick(dot.value, dot.durationTurns)) {
        invalid.push(`${skill.id} (invalid tickConfig.dot)`);
      }
      if (!DOT_SUBTYPES.has(dot.subtype)) {
        invalid.push(`${skill.id} (tickConfig.dot.subtype must be one of ${[...DOT_SUBTYPES].join('|')}, got '${dot.subtype}')`);
      }
      if (dot.stackPolicy && !VALID_POLICIES.has(dot.stackPolicy)) {
        invalid.push(`${skill.id} (tickConfig.dot.stackPolicy invalid: '${dot.stackPolicy}')`);
      }
    }
  }

  if (missing.length > 0) {
    throw new Error(`HoT/DoT skills must define tickConfig: ${missing.join(', ')}`);
  }
  if (invalid.length > 0) {
    throw new Error(`HoT/DoT tickConfig contains invalid values: ${invalid.join(', ')}`);
  }
}

validateTickConfigCoverage();

/**
 * Flat skill registry built once at module load. Includes every class skill,
 * enemy skill, and gear ability — keyed by ID. The combat engine resolves all
 * skills through this single map via `getSkillById`; there is no per-source
 * branching, no fall-through chain, no second registry.
 *
 * Gear abilities live in their own file (GearAbilityDefinitions.ts) for
 * organisation, but get merged in here so the engine sees one canonical
 * lookup. Collisions across sources throw at module load — IDs must be
 * globally unique across class skills, enemy skills, and gear abilities.
 */
const ALL_SKILLS_INDEX: Record<string, SkillDefinition | PassiveDefinition> = (() => {
  const index: Record<string, SkillDefinition | PassiveDefinition> = {};
  const sources: Record<string, string> = {};
  const register = (skill: SkillDefinition | PassiveDefinition, source: string) => {
    if (index[skill.id]) {
      throw new Error(
        `Skill ID collision: '${skill.id}' is defined in both '${sources[skill.id]}' and '${source}'`,
      );
    }
    index[skill.id] = skill;
    sources[skill.id] = source;
  };
  for (const cls of PRIMARY_CLASS_DEFINITIONS) {
    for (const s of cls.skills) register(s, `primary:${cls.id}`);
  }
  for (const cls of SECONDARY_CLASS_DEFINITIONS) {
    for (const s of cls.skills) register(s, `secondary:${cls.id}`);
  }
  for (const s of ENEMY_SKILLS) register(s, 'enemy');
  for (const s of Object.values(GEAR_ABILITIES)) register(s, 'gear');
  return index;
})();

/** Lookup a skill by ID across class skills, enemy skills, and gear abilities. */
export function getSkillById(skillId: string): SkillDefinition | PassiveDefinition | undefined {
  return ALL_SKILLS_INDEX[skillId];
}

